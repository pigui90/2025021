package model;

public class Persona {

	String nombre;
	int edad;
	int cedula;

	public Persona() {

	}

	public String getNombre() {
		return this.nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public int getEdad() {
		return this.edad;
	}
	
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	public int getCedula() {
		return this.cedula;
	}
	
	public void setCedula(int cedula) {
		this.cedula = cedula;
	}
	

}
